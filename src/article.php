                <p>

                    Crete is the largest and one of our favourite islands in Greece for its charismatic towns,
                    indescribably clear water and surreal landscapes. Beyond the beach, Shane’s days were spent in
                    the company of the hospitable locals at the opulent Grecotel Amirandes where Picasso originals
                    hang modestly in the restaurants, the open-air amphitheatre is perfect for taking in starry
                    nights and higher end suites come with their own private gym and swimming pool. Exquisite local
                    produce can be found at the hotel’s own Agreco Farm where afternoons are spent sampling the
                    vineyard’s wines and perfectly paired local cheeses, and getting hands-on at the minizoo with
                    younger travellers. And if you’re looking for a vibrant day out, head to Rethymno for
