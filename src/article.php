                <h3>Magic of Mykonos – the Summer hotspot</h3>
                <p>


                    Charlotte, another of our Europe experts, ventured to the glamorous shores of Mykonos where the
                    white-washed Cycladic buildings with their splashes of blue alongside the infamous nightlife
                    make it an attractive summer hotspot for VIPs and celebrities. The family-owned and run Myconian
                    Collection has some of our favourite hotels on the island. While each emit a very distinct
                    personality, they all make the most of the picturesque Aegean Sea views from ultra-cool swimming
                    pools and furnished private balconies, and serve gourmet cuisine inspired by farm-to-table
                    ingredients. Nestled on the hillside of Elia Bay, the 5* Myconian Villa Collection was no
                    exception, greeting Charlotte with personalised round-the-clock service and impossibly chic
                    interiors and she was continuously impressed by the luxury resort’s ability to capture the
                    essence of lavish living. The opulent villas acting as your own private house in Mykonos, 360
                    degree panoramic views from the saltwater infinity pool and world-class Thalasso spa make it a
                    perfect secluded escape for couples and honeymooners.</p>

                <p>When you manage to pull yourself away from the resort – everything you could possibly need or
                    want for an upscale retreat is at your fingertips – Mykonos flaunts plenty of trendy bars and
                    excellent places to eat just 20 minutes away. Little Venice is one of the island’s more romantic
                    spots to eat and drink. The neighbourhood is lined with charming aged houses right on the
                    water’s edge with the crystal waters lapping precariously close to cobblestoned walkways and
                    dining tables. But if you’re looking for the place to be seen – Mykonos is Greece’s answer to an
                    upscale Ibiza after all – we recommend heading to Scorpios or Nammos Beach. These two exclusive
                    clubs open both day and night sit right on the beachfront offering some of the best places to
                    watch the sunset on the island and can often be seen encircled by the rich and famous’ private
                    yachts.
                </p>
